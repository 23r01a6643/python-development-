#3.If you cross a 490meterlong street in 7 minutes, calculate your speed in meters per second.
# Print the answer without any decimal point in it. Hint: Speed = Distance / Time 
distance = 490  # meters
time_minutes = 7  # minutes
# Converting time to seconds
time_seconds = time_minutes * 60
# Calculating  speed
speed = distance / time_seconds  #formule
print(int(speed))  # given   int to print without decimal
